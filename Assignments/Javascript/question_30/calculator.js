function calculator(num1,oprator,num2){

    if(oprator === "+"){
        console.log(num1+num2);
    }
    else if (oprator === "-") {
        console.log(num1-num2);
    }
    else if (oprator === "*") {
        console.log(num1*num2);
    }
    else if (oprator === "/") {
        console.log(num1/num2);
    }
}


calculator(2,"/",2)